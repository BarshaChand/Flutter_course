//Example of toUpperCase() and toLowerCase()
void main() { 
   String fname = "BArsha"; // Here B is capital.
   String lname = "ChAnd"; // Here C and A are capital.
   print("First name in uppercase: ${fname.toUpperCase()}"); 
   print("First name in lowercase: ${fname.toLowerCase()}"); 
   print("Last name  in uppercase: ${lname.toUpperCase()}"); 
   print("Last name  in lowercase: ${lname.toLowerCase()}"); 
}